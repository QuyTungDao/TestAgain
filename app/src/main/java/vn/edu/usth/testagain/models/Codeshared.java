package vn.edu.usth.testagain.models;

public class Codeshared {
    private String airlineName;
    private String airlineIata;
    private String airlineIcao;
    private String flightNumber;
    private String flightIata;
    private String flightIcao;

    public String getAirlineName() { return airlineName; }
    public void setAirlineName(String value) { this.airlineName = value; }

    public String getAirlineIata() { return airlineIata; }
    public void setAirlineIata(String value) { this.airlineIata = value; }

    public String getAirlineIcao() { return airlineIcao; }
    public void setAirlineIcao(String value) { this.airlineIcao = value; }

    public String getFlightNumber() { return flightNumber; }
    public void setFlightNumber(String value) { this.flightNumber = value; }

    public String getFlightIata() { return flightIata; }
    public void setFlightIata(String value) { this.flightIata = value; }

    public String getFlightIcao() { return flightIcao; }
    public void setFlightIcao(String value) { this.flightIcao = value; }
}
